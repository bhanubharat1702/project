	    <h2 class="bigger">
			<p class="bigger">Human Resource Management system (HRMS) is an integrated system to capture the Service particulars of an employee from day one of the service till the end of the service. Depending on the service information, generate the monthly salary bill of employees by the concerned Drawing and Disbursing Officer(DDOs).
It is a web based application; DDOs can access the application through internet to update service particulars and generate monthly salary bills.
The Government of Karnataka has started implementing HRMS during March 2005 with the World Bank Assistance. Project was implemented in phased manner starting from January 2007, covering few districts at each phase. Complete HRMS was rolled out during February 2008. The HRMS helps in to create an exhaustive and accurate database of Government employees for effective administration and improving productivity and efficiency.
There are about 21,000+ Drawing and Disbursing Officers in the State who are responsible for generating the pay bill of around 5,50,000+ employees scattered in 30 districts of the State. These DDOs will generate the salary bills and submit to the concerned treasury every month. There are about 214 Treasury in the State.
The Project is helping the DDOs to generate the pay bill electronically. Easy implementation of any of the Government policy related to salary or service because of the centralized system. The system will help the management in getting various MIS reports in decision making viz. The list of employees getting the annual increments in any given month, number of employees retiring in any month of the year, budget planning and controlling etc. The project is having various module like: Employee basic details, Qualification details, Leave details, Employee service details, etc.
System also helps to generate arrears bills due for an employee.
Advantage-
Single unified, consistent database of all categories viz( State, All India Service, UGC, AICTE, and Judicial) of employees.
Implementation of service rules has become uniform throughout the State.
More accountability and transparency.
Pension settlement becomes very easy.
Saves lot of time for Govt. in getting any employee information for decision making.</p>
			
